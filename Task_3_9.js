function moveEmployees(idFrom, idWhere){
    enterprises.forEach(enterprise => {
        let departmentFrom = enterprise.departments.find (department => department.id ===idFrom)
        let departmentWhere = enterprise.departments.find (department => department.id ===idWhere)
        if(departmentFrom && departmentWhere){
        departmentWhere.employees_count += departmentFrom.employees_count;
        departmentFrom.employees_count = 0;}
    })
}



